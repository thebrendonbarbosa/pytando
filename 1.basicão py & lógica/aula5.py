# tipo de dado bool (boolean)
# ao questionar algo em um programa, só existem 2 respostas possiveis
# sim(true) ou não (false)
# existem vários operadores para "questionar"
# Dentre eles o ==, que é um operador lógico que questionar
# se um valor é igual ao outro
print( 10 == 10)
print( 10 == 11)

print(type(10 == 10))