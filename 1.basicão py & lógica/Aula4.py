#Tipos int e float
# int -> Número inteiro
# o tipo int representa qualquer numero positivo ou negativo inteiro
#int sem sinal é considerado positivo
print(11) #int
print(-11) #int
print(0)

#float -> Número com ponto flutuante
# o tipo float representa qualquer numero positivo ou negativo quebrado
#float sem sinal é considerado positivo
print(1.1)
print(0.0, -1,5)



# a função type mostra o tipo que o python inferiu ao valor
print( type(0.0), type('brendon')  )
print( type('0.0'), type(1)  )
