# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro 
# tipos imutáveis e primitivos:
# str, int, float,bool

print(1 + 1)
print('a' + 'b')


print('1', type('1'))


#converte o tipo str em int
print(int('1'), type(int('1')))

#somar str com int
print(int('1') + 1)
print(float('2') + 2)

#converte em boolean
print(bool(' '))

#converte em str
print(str(11) + 'b')
